package com.github.tommyettinger.random;

/**
 * Lower ratings are better.
 * <pre>
 *  1111111111111111111L or 0x0F6B75AB2BC471C7L rating: 6
 *  3333333333333333333L or 0x2E426101834D5555L rating: 9
 *  5555555555555555555L or 0x4D194C57DAD638E3L rating: 3
 *  7777777777777777777L or 0x6BF037AE325F1C71L rating: 5
 *  1234567890123456789L or 0x112210F47DE98115L rating: 6
 *   987654321987654321L or 0x0DB4DA5F7EF412B1L rating: 3
 *    81985529216486895L or 0x0123456789ABCDEFL rating: 17
 *  1147797409030816545L or 0x0FEDCBA987654321L rating: 29
 *  1384596537706222391L or 0x1337133713371337L rating: 3
 *   -77200289652949265L or 0xFEEDBABEDEADBEEFL rating: 16
 * -5711973387346444681L or 0xB0BAFE77B0BAFE77L rating: 10
 * -4539647386671194451L or 0xC0FFEEC0FFEEDEADL rating: 11
 * -7046029254386353131L or 0x9E3779B97F4A7C15L rating: 6
 * -7046029254386353129L or 0x9E3779B97F4A7C17L rating: 7
 * </pre>
 */
public class MemorableConstantTest {
	public static void main(String[] args) {
		long[] constants = {
			1111111111111111111L,
			1111111111111111111L * 3L,
			1111111111111111111L * 5L,
			1111111111111111111L * 7L,
			1234567890123456789L,
			987654321987654321L,
			0x0123456789ABCDEFL,
			0x0FEDCBA987654321L,
			0x1337133713371337L,
			0xFEEDBABEDEADBEEFL,
			0xB0BAFE77B0BAFE77L,
			0xC0FFEEC0FFEEDEADL,

			0x9E3779B97F4A7C15L,
			0x9E3779B97F4A7C17L
		};

		for(long c : constants){
			System.out.printf("%20dL or 0x%016XL rating: %d\n", c, c, EnhancedRandom.rateGamma(c));
		}
	}
}
